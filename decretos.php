        <h1 class="section-title">Decretos</h1>
          <p>
          Em dezembro de 1991, estava reunida com a diretoria do Ministério Palavra da Fé, Recife, onde este ministério nasceu. Tratávamos de questões estatutárias, a propósito de uma mudança para São Paulo. Repentinamente o Espírito de Deus se moveu em mim e duas porções das Escrituras me vieram ao coração, acompanhadas do comando: <b>“profiram-nas como decreto todos os dias do ano”</b>. Ele nunca me falara nestes termos e interroguei-me sobre o que viria a significar aquela palavra sobre <b>“decretos”</b>.
          <br><br>
          Entendi depois que, numa dada ocasião, o Senhor pode dar-nos uma porção da Sua palavra, num momento específico, para um fim específico, com um propósito específico, com força de profecia.
          <br><br>
          Decreto é uma palavra com força de lei, proferida por quem tem autoridade para fazê-lo, para ser estabelecida pela obediência. O Grande Dicionário Enciclopédico Rideel, define como “Decisão superior emanada do Poder Executivo, com força de lei.”
          <br><br>
          Tudo que sai da boca de Deus é um decreto, pois é emitido por uma autoridade, cuja palavra tem força de lei. Seus decretos são acompanhados do Seu “cumpra-se”.
          <br><br>
          Desde aquela ocasião, o Senhor nos tem dado um decreto cada ano, que é alvo da nossa oração, buscando gerá-lo em nossas vidas e na nossa nação. Proclamamo-lo em fé, em tom profético, crendo que Deus respalda Sua Palavra.
        </p>
