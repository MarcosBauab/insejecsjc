        <h1 class="section-title">Nossos valores</h1>
        <p>
        Valores são aquelas coisas que realmente tem importância para nós, as ideias e crenças que conservamos como especiais. Nossos valores foram sendo adquiridos ao longo da nossa existência em nosso meio ambiente. Muitos deles são negativos. A Palavra de Deus apresenta os verdadeiros valores que devem governar nossa conduta. Os Dez Mandamentos podem ser considerados como uma Declaração Universal de Valores a serem vividos. Portanto, comprometemo-nos a abandonar os valores negativos e abraçar os valores cristãos, dentre eles:</p><br><br>

        <h4>1.    O DECÁLOGO:</h4>
        <p>Adotar os Dez Mandamentos como nosso mais elevado código de ética, enfatizando o amor a Deus e ao próximo como a suprema motivação de sua prática.</p>

        <h4>2.    ALIANÇA</h4>
        <p>Valorizar os relacionamentos com Deus, com a família e os irmãos, dentro dos princípios de aliança.</p>

        <h4>3.    IGREJA</h4>
        <p>Valorizar a Igreja Local como expressão visível da Igreja de Cristo, buscando expressar Seu caráter em nosso modo de ser e cumprir Sua missão de fazer discípulos cristãos.</p>

        <h4>4.    OFICIAIS DA IGREJA</h4>
        <p>Valorizar a estrutura de liderança do Novo Testamento de apóstolos, profetas, evangelistas, pastores e mestres, com a função de equipar os santos como ministros para fazerem discípulos de Jesus e guiá-los à completa maturidade (Ef. 4:15).</p>

        <h4>5.    GRANDE COMISSÃO</h4>
        <p>Valorizar a responsabilidade de cada discípulo cumprir a Grande Comissão em sua geração (Mateus 28:18-20), fazendo discípulos como um modo de viver.</p>

        <h4>6.    EXEMPLO</h4>
        <p>Valorizar o exemplo como a mais elevada forma de discipulado, o que implica em um contato pessoal e construção de relacionamentos próximos de aliança de amor com os discípulos.</p>

        <h4>7.    CÉLULAS</h4>
        <p>Valorizar as células como uma estratégia dada por Deus para o cumprimento da Grande Comissão, a formação do caráter dos discípulos e seu treinamento para o cumprimento da missão.</p>

        <h4>8.    PRESTAÇÃO DE CONTAS</h4>
        <p>Valorizar a prestação de contas uns aos outros, como membros responsáveis de um só Corpo.</p>

        <h4>9.    FAMÍLIA</h4>
        <p>Valorizar a família como a primeira e mais importante célula e um centro de adoração a Deus, pregação do Evangelho e vivência dos valores cristãos.</p>

        <h4>10.  ESPÍRITO SANTO</h4>
        <p>Valorizar uma vida cheia do Espírito Santo, que manifesta na personalidade o seu FRUTO e no serviço cristão as Suas manifestações sobrenaturais (dons), como fonte de capacitação Divina.</p>

        <h4>11.  MULTIPLICAÇÃO</h4>
        <p>Valorizar uma dinâmica de Igreja que resulte numa constante multiplicação de discípulos, líderes e Igrejas, como meio de cumprir a Grande Comissão.</p>

        <h4>12.     TRANSFORMAÇÃO</h4>
        <p>Valorizar a penetração dos discípulos em todos os segmentos da sociedade, vivendo ali os valores do Reino de Deus, com o propósito de sua transformação em todos os seus níveis.</p>

        <h4>13.     ADORAÇÃO</h4>
        <p>Empenhar-nos em edificar os muros da nação através de uma nuvem de adoração e oração em cada lugar, começando em nossas casas e espalhando-se por todas as esferas da sociedade, visando a sua redenção.</p>

        <h4>14.     VIRTUDES</h4>
        <p>Valorizar a prática das virtudes cristãs internas e externas tais como: humildade, obediência, diligência, excelência, trabalho, disciplina, verdade, pontualidade, justiça, perdão e o amor, que “é o vínculo da perfeição.”</p>

        <h4>15.     ENSINO</h4>
        <p>Valorizar o ensino de todo o Conselho de Deus para um desenvolvimento harmônico da vida cristã, compreendendo que o ensino sólido da Palavra de Deus é o fundamento firme na formação do cristão que é a reprodução de Jesus Cristo, em Seu caráter e em Sua missão.</p>
